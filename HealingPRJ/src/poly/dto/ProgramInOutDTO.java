package poly.dto;

public class ProgramInOutDTO {
	private String agency;
	private String openday;
	private String programName;
	private String teacher;
	private String bunya;
	private String in_tutor;
	private String out_tutor;
	
	public String getAgency() {
		return agency;
	}
	public void setAgency(String agency) {
		this.agency = agency;
	}
	public String getOpenday() {
		return openday;
	}
	public void setOpenday(String openday) {
		this.openday = openday;
	}
	public String getProgramName() {
		return programName;
	}
	public void setProgramName(String programName) {
		this.programName = programName;
	}
	public String getTeacher() {
		return teacher;
	}
	public void setTeacher(String teacher) {
		this.teacher = teacher;
	}
	public String getBunya() {
		return bunya;
	}
	public void setBunya(String bunya) {
		this.bunya = bunya;
	}
	public String getIn_tutor() {
		return in_tutor;
	}
	public void setIn_tutor(String in_tutor) {
		this.in_tutor = in_tutor;
	}
	public String getOut_tutor() {
		return out_tutor;
	}
	public void setOut_tutor(String out_tutor) {
		this.out_tutor = out_tutor;
	}
}
